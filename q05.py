#QUESTÃO 05: ocê está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?  

### RESPOSTA

# 1 - Primeira ida: 
# - Ligaria o primeiro interruptor e deixe-o ligado por alguns minutos. 
# - Após alguns minutos, desligo o primeiro interruptor e ligo o segundo interruptor. 
# - Iria até a sala das lâmpadas. 
# 2- Na sala das lâmpadas: 
# - A lâmpada que está acesa é controlada pelo segundo interruptor (que está ligado). 
# - Tocaria nas outras duas lâmpadas: 
# - A lâmpada que está quente é controlada pelo primeiro interruptor (que estava ligado e foi desligado).
# - A lâmpada que está fria é controlada pelo terceiro interruptor (que nunca foi ligado).